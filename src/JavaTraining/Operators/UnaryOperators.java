package JavaTraining.Operators;

public class UnaryOperators {
    public static void main(String[] args) {
        for(int i=0;i<10;i++){            //Increment
            System.out.println(i);
        }
        System.out.println("");
        for(int i=10;i>0;i--){            //Decrement
            System.out.println(i);
        }
    }
}
